// form (normalized) -> set of canonical full names it can stand for.
const formToCanonicals = new Map<string, Set<string>>();
for (const group of GROUPS) {
  const canonical = group[0]!;
  for (const form of group) {
    const key = normalizeName(form);
    let set = formToCanonicals.get(key);
    if (!set) {
      set = new Set();
      formToCanonicals.set(key, set);
    }
    set.add(canonical);
  }
}

function firstToken(name: string): string {
  return normalizeName(name).split(' ')[0] ?? '';
}

/** Canonical group names a name's first token can stand for (empty if unknown). */
export function canonicalsOf(name: string): Set<string> {
  return formToCanonicals.get(firstToken(name)) ?? new Set();
}

/**
 * Do two names refer to the same given name through the diminutive dictionary?
 * e.g. sameGivenName("Миха", "Михаил Иванов") === true.
 */
export function sameGivenName(a: string, b: string): boolean {
  const ca = canonicalsOf(a);
  if (ca.size === 0) return false;
  for (const x of canonicalsOf(b)) if (ca.has(x)) return true;
  return false;
}
